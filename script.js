const phrases = [
  "Mining in India?",
  "What is the demand for skilled labor in the mining sector?",
  "you're going to realize",
  "How are mining companies attracting and training the next generation of miners?",
  "What are the current market trends for the minerals or metals being mined?",
  "What strategies are mining companies using to manage market volatility?",
  "A C K N O W L E D G E",
];

const el = document.querySelector(".text");
const fx = new TextScramble(el);

let counter = 0;
const next = () => {
  fx.setText(phrases[counter]).then(() => {
    setTimeout(next, 800);
  });
  counter = (counter + 1) % phrases.length;
};

next();
